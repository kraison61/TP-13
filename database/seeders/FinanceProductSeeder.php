    private function placeholderImg(string $name, string $color): string
    {
        $hex = ltrim($color, '#');

        return 'https://placehold.co/600x120/'.$hex.'/ffffff?text='.rawurlencode($name);
    }

    private function iconFor(string $type): string
    {
        return match (true) {
            str_contains($type, 'บัตรเครดิต') => 'bi-credit-card-fill',
            str_contains($type, 'บัตรกดเงินสด') => 'bi-credit-card-2-front-fill',
            str_contains($type, 'บ้าน') => 'bi-house-fill',
            str_contains($type, 'ทะเบียนรถ') || str_contains($type, 'รถ') => 'bi-car-front-fill',
            str_contains($type, 'LINE') => 'bi-line',
            default => 'bi-cash-stack',
        };
    }
